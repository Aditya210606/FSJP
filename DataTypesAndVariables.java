public class DataTypesAndVariables {
    public static void main(String[] args) {
        int num = 12;
        float floatvalue = 12.34f;
        byte bytevalue = 127;                         // byte value range is from -127 to 127 
        short shortvalue = 12345;                     // short value range is 5 digits
        long longvalue = 1234566775;                  // long value range is 10 digits 
        String stringvalue = "Aditya Manoj Patil";    // multiple character are used to from string  
        char charctervalue  = 'A';                    // single character is used in char (always in ' ' quotes)

        System.out.println(num);
        System.out.println(floatvalue);
        System.out.println(bytevalue);
        System.out.println(shortvalue);
        System.out.println(longvalue);
        System.out.println(stringvalue);
        System.out.println(charctervalue);
        }
}
